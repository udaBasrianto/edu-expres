const CACHE_NAME = 'eduhsi-v1';
const urlsToCache = [
  '/',
  '/?tab=home',
  '/?tab=academy',
  '/?tab=cart',
  '/?tab=profile',
  '/manifest.json'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        if (response) {
          return response;
        }
        return fetch(event.request);
      })
  );
});